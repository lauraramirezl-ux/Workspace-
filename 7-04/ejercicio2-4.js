let contador = 10;

console.log("Valor inicial:", contador);

console.log("Post-incremento (contador++):", contador++);
console.log("Valor después del post-incremento:", contador);

console.log("Pre-incremento (++contador):", ++contador);
console.log("Valor después del pre-incremento:", contador);

console.log("Valor después del post-decremento:", contador);

console.log("Pre-decremento (--contador):", --contador);
console.log("Valor después del pre-decremento:", contador);